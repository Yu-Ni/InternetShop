package com.cqut.model.pojo;

public class Admin {
	
	private String adminid;
	private String username;
	private String realname;
	private String password;
	private String telphone;
	private String avatar;
	private int sex;
	
	public Admin() {
		super();
	}
	
	public Admin(String username, String realname, String password,
			String telphone, String avatar, int sex) {
		super();
		this.username = username;
		this.realname = realname;
		this.password = password;
		this.telphone = telphone;
		this.avatar = avatar;
		this.sex = sex;
	}

	public Admin(String adminid, String username, String realname,
			String password, String telphone, String avatar, int sex) {
		super();
		this.adminid = adminid;
		this.username = username;
		this.realname = realname;
		this.password = password;
		this.telphone = telphone;
		this.avatar = avatar;
		this.sex = sex;
	}
	public String getAdminid() {
		return adminid;
	}
	public void setAdminid(String adminid) {
		this.adminid = adminid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getRealname() {
		return realname;
	}
	public void setRealname(String realname) {
		this.realname = realname;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getTelphone() {
		return telphone;
	}
	public void setTelphone(String telphone) {
		this.telphone = telphone;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	@Override
	public String toString() {
		return "Admin [adminid=" + adminid + ", username=" + username
				+ ", realname=" + realname + ", password=" + password
				+ ", telphone=" + telphone + ", avatar=" + avatar + ", sex="
				+ sex + "]";
	}
	
	
	
}
