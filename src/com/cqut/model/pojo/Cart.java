package com.cqut.model.pojo;

import java.util.Date;

public class Cart {
	
	private String cartid;
	private String productid;
	private String buyerid;
	private int number;
	private Date time;
	public Cart() {
		super();
	}
	public Cart(String productid, String buyerid, int number, Date time) {
		super();
		this.productid = productid;
		this.buyerid = buyerid;
		this.number = number;
		this.time = time;
	}
	public Cart(String cartid, String productid, String buyerid, int number,
			Date time) {
		super();
		this.cartid = cartid;
		this.productid = productid;
		this.buyerid = buyerid;
		this.number = number;
		this.time = time;
	}
	public String getCartid() {
		return cartid;
	}
	public void setCartid(String cartid) {
		this.cartid = cartid;
	}
	public String getProductid() {
		return productid;
	}
	public void setProductid(String productid) {
		this.productid = productid;
	}
	public String getBuyerid() {
		return buyerid;
	}
	public void setBuyerid(String buyerid) {
		this.buyerid = buyerid;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	@Override
	public String toString() {
		return "Cart [cartid=" + cartid + ", productid=" + productid
				+ ", buyerid=" + buyerid + ", number=" + number + ", time="
				+ time + "]";
	}
	
}
