package com.cqut.model.pojo;

public class Notice {
	private String noticeid;
	private String title;
	private String username;
	private String theme;
	private String content;
	private String noticetime;	
	
	public Notice(String title, String username, String theme, String content,
			String noticetime) {
		super();
		this.title = title;
		this.username = username;
		this.theme = theme;
		this.content = content;
		this.noticetime = noticetime;
	}
	
	
	@Override
	public String toString() {
		return "Notice [noticeid=" + noticeid + ", title=" + title
				+ ", username=" + username + ", theme=" + theme + ", content="
				+ content + ", noticetime=" + noticetime + "]";
	}

	
	public Notice() {
		super();
	}

	
	public Notice(String noticeid, String title, String username, String theme,
			String content, String noticetime) {
		super();
		this.noticeid = noticeid;
		this.title = title;
		this.username = username;
		this.theme = theme;
		this.content = content;
		this.noticetime = noticetime;
	}

	
	public String getNoticeid() {
		return noticeid;
	}
	public void setNoticeid(String noticeid) {
		this.noticeid = noticeid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getTheme() {
		return theme;
	}
	public void setTheme(String theme) {
		this.theme = theme;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getNoticetime() {
		return noticetime;
	}
	public void setNoticetime(String noticetime) {
		this.noticetime = noticetime;
	}


}
